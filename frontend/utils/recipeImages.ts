/**
 * 根据菜名和菜品类型获取图片源
 */
export function getRecipeImageSource(title: string, dishType?: string | null): ImageSourcePropType {
  if (!dishType || !title) {
    return defaultImage;
  }

  const key = `${dishType}|${title}`;
  
  if (imageMap[key]) {
    return imageMap[key];
  }

  return defaultImage;
}

